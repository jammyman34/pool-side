import Foundation
import SwiftData

@Model
final class Treatment {

    // MARK: - Identity
    var id: UUID
    var createdAt: Date

    // MARK: - Details
    var chemicalName: String
    var actionDescription: String

    /// Amount to add (e.g. "2.5")
    var amount: Double

    /// Unit string (e.g. "lbs", "oz", "cups", "gallons")
    var unit: String

    /// Step-by-step instructions
    var instructions: String

    /// Urgency level mirrors ChemicalStatus
    var urgencyRaw: String

    // MARK: - Completion
    var isCompleted: Bool
    var completedAt: Date?
    var isSkipped: Bool = false
    var skippedAt: Date?
    var reminderNotificationIdentifier: String?

    // MARK: - Source
    /// true = generated by Foundation Models / rule engine, false = manually added
    var isAIGenerated: Bool

    /// The parameter this treatment targets (e.g. "pH", "freeChlorine")
    var targetParameter: String

    /// Minutes to wait after completing THIS treatment before the next one is ready.
    /// 0 = no wait required. Used to schedule local notifications.
    var minutesBeforeNext: Int

    /// Display order within a treatment plan (lower = first)
    var sortOrder: Int

    // MARK: - Expected Effect
    var expectedEffectParameter: String = ""
    var expectedDelta: Double = 0
    var effectDelayHours: Int = 0
    var effectDurationHours: Int = 0
    var doNotRepeatBefore: Date?

    // MARK: - Relationship
    var poolTest: PoolTest?

    // MARK: - Init
    init(
        id: UUID = UUID(),
        createdAt: Date = Date(),
        chemicalName: String,
        actionDescription: String,
        amount: Double,
        unit: String,
        instructions: String,
        urgency: TreatmentUrgency = .recommended,
        isCompleted: Bool = false,
        completedAt: Date? = nil,
        isSkipped: Bool = false,
        skippedAt: Date? = nil,
        reminderNotificationIdentifier: String? = nil,
        isAIGenerated: Bool = true,
        targetParameter: String = "",
        minutesBeforeNext: Int = 0,
        sortOrder: Int = 0,
        expectedEffectParameter: String = "",
        expectedDelta: Double = 0,
        effectDelayHours: Int = 0,
        effectDurationHours: Int = 0,
        doNotRepeatBefore: Date? = nil,
        poolTest: PoolTest? = nil
    ) {
        self.id = id
        self.createdAt = createdAt
        self.chemicalName = chemicalName
        self.actionDescription = actionDescription
        self.amount = amount
        self.unit = unit
        self.instructions = instructions
        self.urgencyRaw = urgency.rawValue
        self.isCompleted = isCompleted
        self.completedAt = completedAt
        self.isSkipped = isSkipped
        self.skippedAt = skippedAt
        self.reminderNotificationIdentifier = reminderNotificationIdentifier
        self.isAIGenerated = isAIGenerated
        self.targetParameter = targetParameter
        self.minutesBeforeNext = minutesBeforeNext
        self.sortOrder = sortOrder
        self.expectedEffectParameter = expectedEffectParameter
        self.expectedDelta = expectedDelta
        self.effectDelayHours = effectDelayHours
        self.effectDurationHours = effectDurationHours
        self.doNotRepeatBefore = doNotRepeatBefore
        self.poolTest = poolTest
    }

    var urgency: TreatmentUrgency {
        get { TreatmentUrgency(rawValue: urgencyRaw) ?? .recommended }
        set { urgencyRaw = newValue.rawValue }
    }
}

// MARK: - Product Swap

/// Categories of swappable chemical products tied to a Treatment.
/// Used by the in-plan product picker; only categories with ≥2 options are listed.
enum ChemicalProductCategory: String, CaseIterable, Identifiable {
    case chlorine
    case pHIncreaser
    case pHDecreaser
    case stabilizer

    var id: String { rawValue }

    var sheetTitle: String {
        switch self {
        case .chlorine: return "Chlorine"
        case .pHIncreaser: return "pH Increaser"
        case .pHDecreaser: return "pH Decreaser"
        case .stabilizer: return "Stabilizer"
        }
    }

    var optionDisplayNames: [String] {
        switch self {
        case .chlorine: return ChlorinePreference.allCases.map(\.displayName)
        case .pHIncreaser: return PHIncreaserPreference.allCases.map(\.displayName)
        case .pHDecreaser: return PHDecreaserPreference.allCases.map(\.displayName)
        case .stabilizer: return StabilizerPreference.allCases.map(\.displayName)
        }
    }

    func currentSelection(from config: PoolConfiguration) -> String {
        switch self {
        case .chlorine: return config.chlorinePreference.displayName
        case .pHIncreaser: return config.pHIncreaserPreference.displayName
        case .pHDecreaser: return config.pHDecreaserPreference.displayName
        case .stabilizer: return config.stabilizerPreference.displayName
        }
    }

    func configApplying(selection: String, to config: PoolConfiguration) -> PoolConfiguration {
        var updated = config
        switch self {
        case .chlorine:
            if let value = ChlorinePreference.allCases.first(where: { $0.displayName == selection }) {
                updated.chlorinePreference = value
            }
        case .pHIncreaser:
            if let value = PHIncreaserPreference.allCases.first(where: { $0.displayName == selection }) {
                updated.pHIncreaserPreference = value
            }
        case .pHDecreaser:
            if let value = PHDecreaserPreference.allCases.first(where: { $0.displayName == selection }) {
                updated.pHDecreaserPreference = value
            }
        case .stabilizer:
            if let value = StabilizerPreference.allCases.first(where: { $0.displayName == selection }) {
                updated.stabilizerPreference = value
            }
        }
        return updated
    }
}

extension Treatment {
    var isWatchlistItem: Bool {
        urgency == .advisory
    }

    var isAcidTreatment: Bool {
        let name = chemicalName.lowercased()
        return name.contains("muriatic acid")
            || name.contains("dry acid")
            || name.contains("ph decreaser")
            || (targetParameter == "pH" && expectedDelta < 0)
            || (targetParameter == "totalAlkalinity" && expectedDelta < 0 && name.contains("acid"))
    }

    /// Returns the swappable product category for this treatment, or nil when
    /// no alternative products exist (hardcoded treatments, visual indicators,
    /// or single-option categories like alkalinity / calcium increasers).
    var productCategory: ChemicalProductCategory? {
        switch targetParameter {
        case "freeChlorine":
            return ChlorinePreference.allCases.contains(where: { $0.displayName == chemicalName }) ? .chlorine : nil
        case "pH":
            if PHIncreaserPreference.allCases.contains(where: { $0.displayName == chemicalName }) {
                return .pHIncreaser
            }
            if PHDecreaserPreference.allCases.contains(where: { $0.displayName == chemicalName }) {
                return .pHDecreaser
            }
            return nil
        case "cyanuricAcid":
            return StabilizerPreference.allCases.contains(where: { $0.displayName == chemicalName }) ? .stabilizer : nil
        default:
            return nil
        }
    }
}

// MARK: - TreatmentUrgency

enum TreatmentUrgency: String, CaseIterable, Codable {
    case immediate   = "immediate"   // Critical — act now
    case recommended = "recommended" // Low/High — take action
    case optional    = "optional"    // Slightly off — minor adjustment
    case advisory    = "advisory"    // Monitor / avoid overcorrection

    var displayName: String {
        switch self {
        case .immediate:   return "Act Now"
        case .recommended: return "Recommended"
        case .optional:    return "Optional"
        case .advisory:    return "Advisory"
        }
    }

    var sortOrder: Int {
        switch self {
        case .immediate:   return 0
        case .recommended: return 1
        case .optional:    return 2
        case .advisory:    return 3
        }
    }
}
